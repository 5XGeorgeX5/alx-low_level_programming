/**
 * add - add 2 numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub 2 numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul 2 numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div 2 numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod 2 numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 */

int mod(int a, int b)
{
	return (a % b);
}
